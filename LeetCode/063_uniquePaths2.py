class Solution:
    def uniquePathsWithObstacles(self, obstacleGrid):
        """
        :type obstacleGrid: List[List[int]]
        :rtype: int
        """
        if obstacleGrid[0][0] == 1 or obstacleGrid[-1][-1] == 1:
            return 0
        height, width = len(obstacleGrid), len(obstacleGrid[0])
        paths = [[-1]*width for _ in range(height)]

        flag = 0
        for i in range(height-1,-1,-1):
            if obstacleGrid[i][-1] == 1:
                flag = 1
            paths[i][-1] = 1 if flag == 0 else 0
        flag = 0
        for i in range(width-1,-1,-1):
            if obstacleGrid[-1][i] == 1:
                flag = 1
            paths[-1][i] = 1 if flag == 0 else 0

        def uniquepaths(x,y):
            if paths[x][y] != -1:
                return paths[x][y]
            if obstacleGrid[x][y] == 1:
                return 0
            paths[x][y] = uniquepaths(x+1, y) + uniquepaths(x, y+1)
            return paths[x][y]

        return uniquepaths(0, 0)


